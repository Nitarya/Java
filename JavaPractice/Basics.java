public class Basics {
    public static void main(String args[]) {
      int num = 3;
      if (num == 0) {
        System.out.println("Value is 0");
      } else if ( num % 2 == 0) {
        System.out.println("Value is even");
      } else {
        System.out.println("Value is odd");
      }
    }
}